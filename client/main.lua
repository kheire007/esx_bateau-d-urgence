AddEventHandler('esx_emergencyBoat:hasEnteredMarker', function(spawner, part, partNum)

  if part == 'VehicleSpawner' then
    CurrentAction     = 'menu_vehicle_spawner'
    CurrentActionMsg  = _U('vehicle_spawner')
    CurrentActionData = {spawner = spawner, partNum = partNum}
  end

end)

AddEventHandler('esx_emergencyBoat:hasExitedMarker', function(spawner, part, partNum)
  ESX.UI.Menu.CloseAll()
  CurrentAction = nil
end)

